SP.getLang = function getLang() {
  const lang = document.documentElement.getAttribute("lang") || "bg";
  return lang.toLowerCase().startsWith("en") ? "en" : "bg";
};

SP.t = function t(key) {
  const lang = SP.getLang();
  return (SP.I18N[lang] && SP.I18N[lang][key]) || key;
};

SP.formatCurrency = function formatCurrency(value, currency = "EUR") {
  const lang = SP.getLang() === "bg" ? "bg-BG" : "en-US";
  return new Intl.NumberFormat(lang, {
    style: "currency",
    currency,
    minimumFractionDigits: 2,
    maximumFractionDigits: 2
  }).format(value || 0);
};

SP.escapeHtml = function escapeHtml(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
};
